package week1.day1;

public class LearnNegativeNumber {

	public static void main(String[] args) {
		int number = -1;

		if (number > 0) {
			System.out.println("number is possitive :" + number);
		} else if (number == 0) {
			System.out.println("number is Zero :" + number);
		} else {
			System.out.println("number is negative :" + number);
		}
	}

}
